    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        Reservation reserve = new Reservation();
        String boxDate = reserveDateComboBox.getSelectedItem().toString();
        if( reserveSeatsTextField.getText().length() > 0 ) {
            int returns = reserve.reserveRoom(reserveFacultyComboBox.getSelectedItem().toString(), boxDate, Integer.parseInt(reserveSeatsTextField.getText()));
            if( returns == 2) {
                reserveStatusLabel.setText("Reservation added to waitlist.");
            } else  if( returns == 1 ) {
                reserveStatusLabel.setText("Reservation added to reservations.");
            } else if( returns < 1 ) {
                reserveStatusLabel.setText("Reservation not valid.");
            }
        } else {
            reserveStatusLabel.setText("Please enter required number of seats.");
        }
        displayTable();
        displayReservation();
        rebuildRemoveReservationComboBox();
        rebuildWaitlistRemoverComboBoxes();
    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

    private void addFacultyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFacultyButtonActionPerformed
        // Add the faculty to the faculty table.
        String name = addFacultyTextField.getText();
        if(name.length() >= 1 && !Faculty.getFacultyList().contains(name)) {
            Faculty.addFaculty(name);
            addFacultyStatusLabel.setText(name+" has been added to the Faculty.");
        } else {
            addFacultyStatusLabel.setText("Name is not valid.");
        }

        // rebuild the reservation faculty combo box.
        rebuildFacultyComboBoxes();
        rebuildFacultyDeleteComboBox();
    }//GEN-LAST:event_addFacultyButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if( Dates.addDate(Integer.parseInt(monthComboBox.getSelectedItem().toString()), Integer.parseInt(dayTextField.getText()), Integer.parseInt(yearTextField.getText())) > 0 ) {
            rebuildMonthComboBox();
            rebuildDateComboBoxes();
            addDateStatus.setText("Date Added");
        } else {
            addDateStatus.setText("Date is not applicable");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

