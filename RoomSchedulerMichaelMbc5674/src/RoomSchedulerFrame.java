    public void displayTable() {
        Waitlist waitlist = new Waitlist();
        ArrayList<String> faculty = waitlist.getFaculty();
        ArrayList<Integer> seats = waitlist.getSeats();
        ArrayList<Date> dates = waitlist.getDates();
        ArrayList<Timestamp> times = waitlist.getTimes();
        int size = times.size();
        Object[][] data = new Object[size][4];
        Object [] columns = {"Faculty", "Seats", "Date", "Timestamp"};
        for( int i = 0; i < size; i++ ) {
            data[i][0] = faculty.get(i);
            data[i][1] = seats.get(i);
            data[i][2] = dates.get(i);
            data[i][3] = times.get(i);
        }
        javax.swing.JTable jTable3 = new javax.swing.JTable(data, columns);
        jTable2.setModel(jTable3.getModel());
    }
    
    public void displayReservation() {
        Reservation reservation = new Reservation();
        ArrayList<String> rooms = reservation.getReservedRooms();
        ArrayList<String> faculty = reservation.getFacultyList();
        ArrayList<Integer> seats = reservation.getReservedSeats();
        ArrayList<Date> dates = reservation.getAllDates();
        ArrayList<Timestamp> times = reservation.getReservedTimes();
        int size = times.size();
        Object[][] data = new Object[size][5];
        Object [] columns = {"Faculty", "Seats", "Date", "Timestamp", "Room"};
        for( int i = 0; i < size; i++ ) {
            data[i][0] = faculty.get(i);
            data[i][1] = seats.get(i);
            data[i][2] = dates.get(i);
            data[i][3] = times.get(i);
            data[i][4] = rooms.get(i);
        }
        javax.swing.JTable jTable3 = new javax.swing.JTable(data, columns);
        reservationTable.setModel(jTable3.getModel());
    }
    
